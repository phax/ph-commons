package com.helger.graph.impl;

import javax.annotation.Nonnull;
import javax.annotation.Nullable;
import javax.annotation.concurrent.NotThreadSafe;

import com.helger.commons.annotation.Nonempty;
import com.helger.commons.collection.attr.MapBasedAttributeContainerAny;
import com.helger.commons.hashcode.HashCodeGenerator;
import com.helger.commons.string.StringHelper;
import com.helger.commons.string.ToStringGenerator;
import com.helger.graph.IMutableBaseGraphObject;

/**
 * Base class for graph nodes and graph relations.
 *
 * @author Philip Helger
 */
@NotThreadSafe
public abstract class AbstractBaseGraphObject extends MapBasedAttributeContainerAny <String>
                                              implements IMutableBaseGraphObject
{
  private final String m_sID;

  /**
   * Constructor
   *
   * @param sID
   *        If <code>null</code> a new ID is generated by the
   *        {@link GraphObjectIDFactory}.
   */
  public AbstractBaseGraphObject (@Nullable final String sID)
  {
    if (StringHelper.hasNoText (sID))
      m_sID = GraphObjectIDFactory.createNewGraphObjectID ();
    else
      m_sID = sID;
  }

  @Nonnull
  @Nonempty
  public final String getID ()
  {
    return m_sID;
  }

  @Override
  public boolean equals (final Object o)
  {
    if (o == this)
      return true;
    if (!super.equals (o))
      return false;
    final AbstractBaseGraphObject rhs = (AbstractBaseGraphObject) o;
    return m_sID.equals (rhs.m_sID);
  }

  @Override
  public int hashCode ()
  {
    return HashCodeGenerator.getDerived (super.hashCode ()).append (m_sID).getHashCode ();
  }

  @Override
  public String toString ()
  {
    return ToStringGenerator.getDerived (super.toString ()).append ("id", m_sID).getAsString ();
  }
}
